    public double[] getPIDValores(){
        theresError = false;
        double p = 0, i = 0, d = 0;
        
        double[] param = new double[3];
        try{
            p = Double.parseDouble(this.getKpValor());
        } catch (Exception e) {
            theresError = true;
            erro += "Valor de Kp inv√°lido.\n";
        }
        
        if(this.getKiValor().isEmpty() && this.getTiValor().isEmpty()){
            i = 0;
        }
        else{
            if("NaN".equals(this.getKiValor())){
                try{
                    i = Double.parseDouble(this.getTiValor());
                } catch (Exception e) {
                    theresError = true;
                    erro += "Valor de Ti inv√°lido.\n";
                }
            }
            else{
                try{
                    i = Double.parseDouble(this.getKiValor());
                } catch (Exception e) {
                    theresError = true;
                    erro += "Valor de Ki inv√°lido.\n";
                }
            }
        }
        
        if(this.getKdValor().isEmpty() && this.getTdValor().isEmpty()){
            d = 0;
        }
        else{
            if("NaN".equals(this.getKdValor())){
                try{
                    d = Double.parseDouble(this.getTdValor());
                } catch (Exception e) {
                    theresError = true;
                    erro += "Valor de Td inv√°lido.\n";
                }
            }
            else{
                try{
                    i = Double.parseDouble(this.getKdValor());
                } catch (Exception e) {
                    theresError = true;
                    erro += "Valor de Kd inv√°lido.\n";
                }
            }
        }
        
        param[0] = p;
        param[1] = i;
        param[2] = d;
        
        return param;
    }
    
